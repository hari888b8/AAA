// ─── Runtime API ─────────────────────────────────────────────────────────────

let currentLang = localStorage.getItem('agrihub_lang') || 'en';

export function getLang() { return currentLang; }

export function setLang(code) {
  currentLang = code;
  localStorage.setItem('agrihub_lang', code);
}

export function t(key) {
  return (translations[currentLang] || translations.en)[key] || translations.en[key] || key;
}

export function getLangName(code) {
  return LANGUAGES.find(l => l.code === code)?.native || code;
}
